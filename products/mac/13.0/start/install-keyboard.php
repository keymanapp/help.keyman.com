    <div class="titlepage">
        <h2 class="title" style="clear: both" id="start_download-install_keyboard">How To - Download and Install a Keyman Keyboard</h2>
    </div>
    <p>You must have a Keyman keyboard installed to type with Keyman for macOS in your language.</p>
    <h3><a name="id467995"></a><a name="install_tav"></a>Downloading &amp; Installing a Keyman Keyboard within Keyman for macOS</h3>
    <p>Here's how to download and install a Keyman keyboard within Keyman for macOS:</p>
    <div class="orderedlist"><ol type="1">
        <li><p>Choose Keyman from the Input menu in the menu bar.</p></li>
        <li><p>Open Keyman Configuration, from the Configuration link under Keyman in the Input menu. Note: If you just installed Keyman and the Configuration menu is not available, please restart the computer.</p>
        <p><span class="inlinemediaobject"><img src="images/keyman_menu_config.png"></span></p></li>
        <li><p>Select the Keyboard Layouts tab.</p></li>
        <li><p>Click <span class="guibutton">Download keyboard…</span>.</p></li>
        <li><p>From the dropdown boxes, find a language associated with your keyboard and click <span class="guibutton">Search</span>.</p></li>
        <li><p>Click <span class="guibutton">Install Keyboard</span> for the keyboard you want to install.</p>
            <div class="tip" style="margin-left: 0.5in; margin-right: 0.5in;"><h3 class="title">Tip</h3>
                <p>The icons to the right of each keyboard name show valuable information about what the keyboard includes:</p>
                <div class="itemizedlist"><ul type="disc">
                    <li><span class="inlinemediaobject"><img src="images/icon-unicode.png"></span> – means that the keyboard is a Unicode keyboard.</li>
                    <li><span class="inlinemediaobject"><img src="images/icon-osk.png"></span> – means the keyboard has a virtual On Screen Keyboard.</li>
                    <li><span class="inlinemediaobject"><img src="images/icon-help.png"></span> – means the keyboard contains help documentation.</li>
                    <li><span class="inlinemediaobject"><img src="images/icon-fonts.png"></span> – means the keyboard comes with fonts.</li>
                    <li><span class="inlinemediaobject"><img src="images/icon-intro.png"></span> – means the keyboard has introductory help.</li>
                </ul></div>
            </div>
            <div class="note" style="margin-left: 0.5in; margin-right: 0.5in;"><h3 class="title">Note</h3>
            <p>If the keyboard you want doesn't have an <span class="guibutton">Install Keyboard</span> button, then the keyboard has been made available from a third-party site. You can download the keyboard by following the links provided. If the keyboard doesn't install automatically, follow the instructions below to <a class="link" href="start_download-install_keyboard.php#install_folder">install a keyboard from your computer</a>.</p></div></li>
        <li><p>Click <span class="guibutton">Done</span>. The keyboard will be installed automatically and you will see a Keyboard/Package Info window.</p>
            <div class="tip" style="margin-left: 0.5in; margin-right: 0.5in;"><h3 class="title">Tip</h3>
                <p>The Keyboard/Package Info window includes some helpful information about the keyboard you have installed. In addition to the name and version of the keyboard, the dialog may also show:</p>
                <div class="itemizedlist"><ul type="disc">
                    <li><p>Fonts installed with the keyboard.</p></li>
                    <li><p>Author name and contact information.</p></li>
                    <li><p>Specific help and readme information (under the readme tab).</p></li>
                    <li><p>Other details.</p></li>
                 </ul></div>
            </div>
            <div class="note" style="margin-left: 0.5in; margin-right: 0.5in;"><h3 class="title">Note</h3>
                <p><span class="guibutton">Keyman keyboards are installed for the current user only. You can find the installed keyboards in 'Documents/Keyman-Keyboards' folder; however, you should not delete or modify this folder or its contents.</p>
            </div></li>
        <li><p>To enable/disable a keyboard; click on the check box next to the keyboard.</p></li>
        <li><p>You can now select a Keyman keyboard from the Keyboards menu under Keyman in the Input menu.</p>
            <div class="note" style="margin-left: 0.5in; margin-right: 0.5in;"><h3 class="title">Note</h3>
                <p>Only enabled keyboards will be displayed in the Keyboards menu under Keyman in the Input menu.</p>
            </div></li>
    </ol></div>

    <h3><a name="id468245"></a><a name="install_folder"></a>Installing a Keyman Keyboard from a Folder on Your Computer</h3>
    <p>There are two ways to install a keyboard from your computer. The easiest way (on macOS 10.8 and later) is simply to find the keyboard package (.KMP file) on your computer using Finder and double-click. When prompted whether or not to install the package, click Yes. (If you don't see the dialog box, it might be hiding behind your Finder window.)</p><p>The other way is to drag and drop a keyboard package file onto the Keyman Configuration window. Here's how:</p>
    <div class="orderedlist"><ol type="1">
        <li><p>Choose Keyman from the Input menu in the menu bar.</p></li>
        <li><p>Open Keyman Configuration, from the Configuration link under Keyman in the Input menu.</p>
            <p><span class="inlinemediaobject"><img src="images/keyman_menu_config.png"></span></p></li>
        <li><p>Select the Keyboard Layouts tab.</p></li>
        <li><p>Find the keyboard package on your computer. Keyman keyboard packages have a file name that ends with .KMP.</p></li>
        <li><p>Drag and drop the keyboard package file in the installed keyboards list in Keyman Configuration window.</p>
            <div class="note" style="margin-left: 0.5in; margin-right: 0.5in;"><h3 class="title">Note</h3>
                <p><span class="guibutton">You can drag and drop multiple keyboard packages at once.</p>
            </div>
            <div class="note" style="margin-left: 0.5in; margin-right: 0.5in;"><h3 class="title">Note</h3>
                <p><span class="guibutton">Keyman keyboards are installed for the current user only. You can find the installed keyboards in 'Documents/Keyman-Keyboards' folder; however, you should not delete or modify this folder or its contents.</p>
            </div></li>
        <li><p>To enable/disable a keyboard; click on the check box next to the keyboard.</p></li>
        <li><p>You can now select a Keyman keyboard from the Keyboards menu under Keyman in the Input menu.</p>
            <div class="note" style="margin-left: 0.5in; margin-right: 0.5in;"><h3 class="title">Note</h3>
                <p>Only enabled keyboards will be displayed in the Keyboards menu under Keyman in the Input menu.</p>
            </div></li>
    </ol></div>

