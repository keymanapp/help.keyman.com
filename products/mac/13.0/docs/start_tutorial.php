    <div class="titlepage">
        <h2 class="title" style="clear: both" id="start_tutorial">Getting Started with Keyman for macOS</h2>
    </div>
    <p>Follow these steps for a basic introduction to Keyman for macOS:</p>
    <div class="note" style="margin-left: 0.5in; margin-right: 0.5in;">
        <h3 class="title">Note</h3>
        <p><span class="application">Keyman for macOS</span> must be installed first. For help on installing <span class="application">Keyman for macOS</span>, please see: <a class="xref" href="start_download-install_keyman.php" title="How To - Install Keyman for macOS">How To - Install Keyman for macOS</a></p>
    </div>
    
    <h3><a name="id480782"></a>Step 1</h3>
    <span class="inlinemediaobject"><img src="images/menu_bar_keyman.png"></span>
    <p>Check the menu bar near the date and time to see if Keyman for macOS is active. If it is active you will see the Keyman icon: <span class="inlinemediaobject"><img src="images/icon-keyman.png"></span></p>
    
    <h3><a name="id480755"></a><a name="tutorial_step2"></a>Step 2</h3>
    <div class="itemizedlist"><ul type="disc">
        <li><p>If the Keyman icon is in the menu bar near the date/time, continue on to <a class="link" href="start_tutorial.php#tutorial_step5">Step 5</a>.</p>
            <p><span class="inlinemediaobject"><img src="images/menu_bar_keyman.png"></span></p></li>
        <li><p>If the Keyman icon is not in the menu bar, choose Keyman from the Input menu in the menu bar. The Input menu appears in the menu bar only if you enable at least two input sources in Keyboard preferences. The Input menu is identified by a flag or character that represents the input source currently in use.</p>
            <p><span class="inlinemediaobject"><img src="images/input_menu.png"></span></p></li>
        <li><p>If Keyman is not in the Input menu, Keyman for macOS is not added as an input source. Follow steps 3 and 4 to add Keyman for macOS as an input source.</p></li>
    </ul></div>

    <h3><a name="id481054"></a>Step 3</h3>
    <p>To add Keyman for macOS as an input source:</p>
    <div class="itemizedlist"><ul type="disc">
        <li><p>On the Apple menu, click System Preferences.</p><p><span class="inlinemediaobject"><img src="images/apple_menu.png"></span></p></li>
        <li><p>Click Keyboard, select the Input Sources tab, and then click <span class="inlinemediaobject"><img src="images/add.png"></span>.</p></li>
        <li><p>In the left-hand pane, select Multiple Languages, and then select Keyman in the right-hand pane.</p><p><span class="inlinemediaobject"><img src="images/add_keyman.png"></span></p></li><li><p>Click Add.</p></li>
    </ul></div>

    <h3><a name="id481037"></a>Step 4</h3>
    <div class="itemizedlist"><ul type="disc">
        <li><p>Choose Keyman from the Input menu in the menu bar.</p><p><span class="inlinemediaobject"><img src="images/input_menu.png"></span></p></li>
        <li><p>Keyman for macOS will now be active and the Keyman icon will be in the menu bar near the clock.</p><p><span class="inlinemediaobject"><img src="images/menu_bar_keyman.png"></span></p></li>
    </ul></div>

    <h3><a name="id481042"></a>Step 5</h3>
    <span class="inlinemediaobject"><img src="images/keyboards_menu.png"></span><p>On the Input menu, point to Keyboards, and then select a Keyman keyboard. Note: If the Keyman menu items are not available, please restart the computer.</p>
    <div class="tip" style="margin-left: 0.5in; margin-right: 0.5in;">
    <h3 class="title">Tip</h3>
    <p>If there are no Keyman keyboards in the Keyboards menu, you can learn how to install one here: <a class="xref" href="start_download-install_keyboard.php" title="How To - Download and Install a Keyman Keyboard">How To - Download and Install a Keyman Keyboard</a></p></div>

    <h3><a name="id481076"></a>Step 6</h3>
    <span class="inlinemediaobject"><img src="images/osk_amharic.png"></span><p>To become familiar with the layout of a Keyman keyboard, use the On-Screen Keyboard tool: On the Input menu, point to Keyman, and then click On-Screen Keyboard.</p>
    <div class="tip" style="margin-left: 0.5in; margin-right: 0.5in;">
    <h3 class="title">Tip</h3>
    <p>Some Keyman keyboards do not include an On-Screen Keyboard. You will see a standard US keyboard layout if the selected keyboard does not include an On-Screen Keyboard.</p></div>

    <h3><a name="id481102"></a>Step 7</h3>
    <p>You can now start typing in your language in almost any application (e.g., TextEdit). Use your hardware keyboard or click the keys in the On-Screen Keyboard to type.</p>
    <h3><a name="id481106"></a>Note</h3>
    <p>To return to using your normal keyboard, choose your default keyboard from the Input menu in the menu bar.</p>
    
