<h2>Overview</h2>

<p>Welcome! In this tutorial, you will learn how to create a <a href="../../../reference/file-types/kmp" title="KMP files">package</a>. A
package is a collection of files, compressed into a single file, just like a ZIP file. It is designed to make installation of
keyboards, fonts and related documentation straightforward for the end user. When done, the package can be installed into Keyman
Desktop through a simple dialog. Keyman for mobile apps can also download and install keyboards through the keyboard package.</p>

<p>We will be creating a Khmer Angkor package. You can make your own package based on the Quick French keyboard from the
  <a href="../../develop/tutorial" title="Keyboard Tutorial">keyboard tutorial</a>.</p>

