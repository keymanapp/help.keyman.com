<h1>Step 1: What do we include?</h1>

<p>What is the purpose of a lexical model package? To make installation of a lexical model as
straightforward as possible for the end user. We need to keep this goal in mind as we work on all the aspects of a package.</p>

<p>A great package will:</p>

<ol type="1">
  <li>Include one lexical model to install.</li>
  <li>Include a 'readme.htm' file which describes why the user would install this package.</li>
  <li>Include a 'welcome.htm' file which is displayed before/after install.</li>
</ol>

