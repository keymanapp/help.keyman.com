<h2>Overview</h2>

<p>Welcome! In this tutorial, you will learn how to create a
lexical model <a href="../../../../reference/file-types/kmp" title="KMP files">package</a>. A
package is a collection of files, compressed into a single file, just like a ZIP file. It is designed to make installation of
a lexical model straightforward for the end user. When done, Keyman for mobile apps can download and install
lexical models through the package.</p>

<p>We will be creating a package for the SENĆOŦEN lexical model that was made in
