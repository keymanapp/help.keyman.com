<p>Branding is the process of putting your own product name, logo and distribution model together for creating a custom product based
on Keyman Engine.</p>

<p>In this tutorial, we will look at how to choose an appropriate licensing model for your product, and adapt the Keyman Desktop user
interface to meet your product's requirements.</p>

<aside>
  <h3 class="title">Note</h3>

  <p>You must purchase a licence for the Branding Pack before you can use the Branding Editor or the Distribution Editor in Keyman
  Developer.</p>
</aside>

<ol>
  <li>To get started with this tutorial, open the Project window, and click the <span class="guibutton">Branding</span> tab.</li>

  <li>Click <span class="guibutton">New Brand Template</span> and enter a filename for the branding source file. A branding source
    file will have a .kct extension. Click <span class="guibutton">OK</span> to create the file.</li>

  <li>A dialog will appear, prompting you to select a template for customisation. You should select the "keyman" template
    for this tutorial, and click <span class="guibutton">OK</span>.</li>
</ol>

